VOID drawmole(i)
int i;
{
    static int pat[7] = {
	PFACE, PBODY, PBODY, PBOO, PFACE, PHIDE, PKYU };
    int x;

    x = pat[Mole[i].st] * 32;
    cpyv2v(x, 0, x+31, 31, 1, Molex[i], Moley[i], 0, 0);
}

VOID movemole()
{
    int i, next;
    static int act[3] = { SBODY1, SBODY2, SHIDE };
    static int wt[6] = { 5, 12, 10,  6,  8, 25};
    static int wr[6] = {10,  8,  1, 14,  1, 95};

    for (i = 0; i < 10; ++i) {
	if (--Mole[i].ct == 0) {
	    switch (Mole[i].st) {
	      case SFACE1: next = act[rnd(3)]; break;
	      case SBODY1: next = SFACE2;      break;
	      case SBODY2: next = SBOO;	       break;
	      case SBOO:   next = SFACE2;      break;
	      case SFACE2: next = SHIDE;       break;
	      case SHIDE:  next = SFACE1;      break;
	      case SKYU:   next = SFACE2;      break;
	     }
	     Mole[i].st = next;
	     Mole[i].ct = wt[next] + rnd(wr[next]);
	     drawmole(i);
	}
    }
}

VOID drawhammer()
{
    putspr(0, Hmx, Hmy, 0, Hmst);
    putspr(1, Hmx, Hmy, 0, Hmst + 4);
}

VOID transfer()
{
    static int trnx[8] = { 0, 8, 10, 8, 0, -8, -10, -8 };
    static int trny[8] = { -10, -8, 0, 8, 10, 8, 0, -8 };
    TINY i;

    if ((i = gtstck(0)) != 0) {
	Hmx += trnx[i-1];
	Hmy += trny[i-1];
    }
    else {
	gtpad(12);
	Hmx += gtpad(13)/2;
	Hmy += gtpad(14)/2;
    }

    if (Hmx < 8)   Hmx = 8;
    if (Hmx > 216) Hmx = 216;
    if (Hmy < 8)   Hmy = 8;
    if (Hmy > 174) Hmy = 174;
}

VOID hammer()
{
    if (Hmst == HUP) {
	transfer();
	if (gttrig(0) || gttrig(1)) {
	    Hmst = HDOWN;
	    Hmct = 3;
	}
    }
    else {
	if (--Hmct == 0) {
	    Hmst = HUP;
	    gtpad(12);
	}
    }
    drawhammer();
}

VOID prnscore()
{
    int x, y;

    y = ((Score - 1) / 20) * 8;
    x = ((Score - 1) % 20) * 8;
    if (x > 9*8) x += 8;
    cpyv2v(160, 0, 166, 6, 1, 48+x, y, 0, 0);
}

VOID subjudge(i)
int i;
{
    if (Hmx < Molex[i] - 15) return;
    if (Hmx > Molex[i] + 15) return;
    if (Hmy < Moley[i] - 20) return;
    if (Hmy > Moley[i] -  6) return;

    Mole[i].st = SKYU;
    Mole[i].ct = 12;
    ++Score;
    drawmole(i);
    prnscore();

    sound(0, 64);
    sound(1, 1);
    sound(6, 24);
    sound(7, 54);
    sound(8, 16);
    sound(11, 0);
    sound(12, 20);
    sound(13, 0);
}

VOID judge()
{
    int i, s;

    if (Hmst == HDOWN) {
	for (i = 0; i < 10; ++i) {
	    s = Mole[i].st;
	    if (s == SBODY1 || s == SBODY2 || s == SBOO)
		subjudge(i);
	}
    }
}

VOID starting()
{
    int i;

    srnd();
    Hmx = 120;
    Hmy = 100;
    Hmst = HUP;
    Hmct = 0;
    Score = 0;

    for (i = 0; i < 10; ++i) {
	Mole[i].st = SHIDE;
	Mole[i].ct = rnd(100);
	drawmole(i);
    }

    drawhammer();
    boxfil(224, 40, 233, 189, 10,0);
}

VOID game()
{
    int i, j, wait;

    for (i = 0; i < 150; ++i) {
	line(224, 40 + i, 233, 40 + i, 1, 0);
	for (j = 0; j < 10; ++j) {
	    hammer();
	    movemole();
	    judge();
	    for (wait = 0;wait < 1000; ++wait);
	}
    }
}

VOID ending()
{
    int wait, loop;

    for (loop = 9; loop > 0; --loop) {
	palett(12, 6, 1, 1);
	for (wait = 0; wait < 1000; ++wait);
	sound(0, 29 + loop * 24);
	sound(1, 1);
	sound(7, 62);
	sound(8, 16);
	sound(11, 0);
	sound(12, 24);
	sound(13,0);
	palett(12, 1, 4, 1);
	for (wait = 0; wait < 1000; ++wait);
    }

    while (!gttrig(0) && !gttrig(1));
}

